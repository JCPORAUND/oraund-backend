/**
 * 페르소나 + 서브 페르소나 → 최종 시스템 프롬프트 문자열
 * @param {string} persona    'b2c' | 'b2b' | 'unknown'
 * @param {string|null} subpersona  'b2c.gift' 등 | null
 * @returns {string}
 */
function buildSystemPrompt(persona, subpersona) {
  const base = (persona === 'b2b') ? B2B_PROMPT : B2C_PROMPT;
  const hint = subpersona && SUBPERSONA_HINTS[subpersona];
  return hint ? (base + '\n\n' + hint.trim()) : base;
}


module.exports = {
  buildSystemPrompt,
  B2C_PROMPT,
  B2B_PROMPT,
  COFFEE_DATABASE,
  SUBPERSONA_HINTS,
};
