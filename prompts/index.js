/**
 * 페르소나 + 서브 페르소나 → 최종 시스템 프롬프트 문자열
 *
 * 프롬프트 조립 순서:
 *  1. 페르소나별 베이스 (B2C 또는 B2B) — 카탈로그와 링크가 주입됨
 *  2. 서브 페르소나 힌트 (있으면)
 *
 * @param {string} persona    'b2c' | 'b2b' | 'unknown'
 * @param {string|null} subpersona  'b2c.gift' 등 | null
 * @returns {string}
 */
function buildSystemPrompt(persona, subpersona) {
  // 카탈로그 텍스트 (페르소나별 슬라이스)
  const catalogText = formatCatalogForPersona(persona, subpersona);

  // 페이지 안내 URL 블록
  const links = getRelevantLinks(persona, subpersona);
  const linksText = formatLinksForPrompt(links);

  // 베이스 프롬프트 조립
  const base = (persona === 'b2b')
    ? b2bBasePrompt(catalogText, linksText)
    : b2cBasePrompt(catalogText, linksText);

  // 서브 페르소나 힌트 추가
  const hint = subpersona && SUBPERSONA_HINTS[subpersona];
  return hint ? (base + '\n\n' + hint.trim()) : base;
}


